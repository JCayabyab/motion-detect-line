function writeDataToText(disX,disY)
%WRITEDATATOTEXT Takes two graphs and outputs data to text
dlmwrite('displacement.txt',disY,'delimiter','\t','precision',3);
movefile displacement.txt data-X/16/3;

dlmwrite('displacementX.txt',disX,'delimiter','\t','precision',3);
movefile displacementX.txt data-X/15/3;

dlmwrite('displacementX.txt',disX,'delimiter','\t','precision',3);
movefile displacementX.txt data-X/16/3;

dlmwrite('displacementX.txt',disX,'delimiter','\t','precision',3);
movefile displacementX.txt data-X/17/3;

dlmwrite('displacementX.txt',disX,'delimiter','\t','precision',3);
movefile displacementX.txt data-X/18/3;

dlmwrite('displacementX.txt',disX,'delimiter','\t','precision',3);
movefile displacementX.txt data-X/19/3;

dlmwrite('displacementX.txt',disX,'delimiter','\t','precision',3);
movefile displacementX.txt data-X/20/3;

dlmwrite('displacementX.txt',disX,'delimiter','\t','precision',3);
movefile displacementX.txt data-X/21/3;
end
